            <h3> BULU TANGKIS</h3>
            <p class="text-justify"><strong> Bulu Tangkis </strong> Bulu tangkis atau badminton (bahasa Inggris: badminton) adalah suatu olahraga yang menggunakan alat yang berbentuk bulat dengan memiliki rongga-rongga di bagian pemukulnya. Dan memiliki gagang. Alat ini dikenal dengan nama raket yang dimainkan oleh dua orang (untuk tunggal) atau dua pasangan (untuk ganda) yang saling berlawanan.Mirip dengan tenis, bulu tangkis bertujuan memukul bola permainan bulu tangkis, yaitu kok (shuttlecock) melewati jaring agar jatuh di bidang permainan lawan yang sudah ditentukan dan berusaha mencegah lawan melakukan hal yang sama.<br>
            Tiap pemain atau pasangan mengambil posisi berseberangan pada kedua sisi jaring di lapangan bulu tangkis. Permainan dimulai dengan salah satu pemain melakukan servis. Tujuan permainan adalah untuk memukul sebuah kok menggunakan raket, melewati jaring ke wilayah lawan, sampai lawan tidak dapat mengembalikannya kembali. Area permainan berbeda untuk partai tunggal dan ganda. Bila kok jatuh di luar area tersebut maka kok dikatakan "keluar". Setiap kali pemain/pasangan tidak dapat mengembalikan kok (karena menyangkut di jaring atau keluar lapangan) maka lawannya akan memperoleh poin. Permainan berakhir bila salah satu pemain/pasangan telah meraih sejumlah poin tertentu.</p>
