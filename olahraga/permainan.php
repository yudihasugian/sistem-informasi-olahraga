            <p class="text-justify"><strong>Sepak Bola </strong>Pengerian sepak bola adalah merujuk pada permainan yang dilakukan oleh dua tim berbeda, dengan komposisi pemain yang berada lapangan sebanyak sebelas orang. Dimana masing-masing tim berupaya untuk menang dan mencetak gol ke gawang lawan.Dalam pengertian sepak bola kemudian permainan ini melibatkan pergerakan unsur fisik, mental, motorik kasar dan motorik halus, serta di bangun dengan kekuatan tim yang solid. Pergerakan semua unsur tersebut dilakukan untuk menjaga pergerakan bola tetap dinamis dan melewati garis gawang.<br>
