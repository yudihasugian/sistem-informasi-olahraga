            <h3> BOLA VOLLY</h3>
            <p class="text-justify"><strong> Bola Volly </strong> Permainan bola voli termasuk salah satu contoh dari cabang olahraga bola besar. Bisa dikatakan, permainan ini masuk ke dalam kategori pertandingan karena melibatkan dua tim yang saling berhadapan untuk mendapatkan hasil pertandingan.Cara memainkan bola voli adalah dengan memukul bola agar melewati bagian atas net yang ada di tengah lapangan. Agar mendapatkan skor, para pemain dalam satu tim harus mengarahkan bola ke area lawan, sehingga bola terjatuh di daerah pertahanan lawan.<br>
            Pencetusnya adalah William G. Morgan yang merupakan guru pendidikan jasmani di Amerika Serikat. Ia sangat terinspirasi dari permainan bola basket. Inspirasi tersebut dia dapatkan setelah bertemu dengan James Naismith yang merupakan pencetus olahraga bola basket.Awalnya, permainan ini ditujukan untuk para orang tua. Namun, Morgan ingin sekali menyesuaikan antara olahraga dengan kemampuan fisik para orang tua. Sebab tidak memungkinkan bagi orang-orang tua jika harus mengandalkan kekuatan fisik dalam waktu yang lama, misalnya berlari.</p>
